package edu.ucla.wise.persistence.data;

import java.util.Set;

public class GeneratedKeysForDataTables {

    private Set<Integer> integerTableKeys;
    private Set<Integer> textTableKeys;

    public Set<Integer> getIntegerTableKeys() {
        return this.integerTableKeys;
    }

    public void setIntegerTableKeys(Set<Integer> integerTableKeys) {
        this.integerTableKeys = integerTableKeys;
    }

    public Set<Integer> getTextTableKeys() {
        return this.textTableKeys;
    }

    public void setTextTableKeys(Set<Integer> textTableKeys) {
        this.textTableKeys = textTableKeys;
    }

}
